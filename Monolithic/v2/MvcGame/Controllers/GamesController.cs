            game.GameTitle = "Game To Edit For Test";
            game.Genre = "Test Genre Genre";
            game.ReleaseYear = "2022";
            game.Description = "Test edit ";

            if (id != game.GameID)
            {
                return NotFound();
            }

            if (ModelState.IsValid)
            {
                try
                {
                    _context.Update(game);
                    await _context.SaveChangesAsync();
